char	*ft_strupcase(char *str)
{
	int cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (str[cnt] > 96 && str[cnt] <= 122)
		{
			str[cnt] = str[cnt] - 32;
		}
		cnt++;
	}
	return (str);
}
