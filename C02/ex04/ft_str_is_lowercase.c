int		ft_str_is_lowercase(char *str)
{
	int cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (str[cnt] < 97 || str[cnt] > 122)
		{
			return (0);
		}
		cnt++;
	}
	return (1);
}
