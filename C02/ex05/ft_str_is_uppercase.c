int	ft_str_is_uppercase(char *str)
{
	int cnt;

	cnt = 0;
	while (str[cnt] != '\0')
	{
		if (str[cnt] < 65 || str[cnt] > 90)
		{
			return (0);
		}
		cnt++;
	}
	return (1);
}
