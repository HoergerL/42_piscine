int		ft_sqrt(int nb)
{
	int temp;

	temp = 0;
	if (nb < 0)
		return (0);
	if (nb > 2147395600)
		return (0);
	while (temp * temp <= nb)
	{
		temp++;
		if (temp * temp == nb)
		{
			return (temp);
		}
	}
	return (0);
}
